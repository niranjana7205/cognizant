import java.util.*;
public class Operator {
    public static void main(String args[])
    {
        Scanner sc=new Scanner(System.in);
        //Operator Precedence
        //() [] * / % + -

        int a = 10 + 5 * 2;
        int b = (10 + 5) * 2;
        int c = 20 - 4 / 2 + 3;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
    }
}








